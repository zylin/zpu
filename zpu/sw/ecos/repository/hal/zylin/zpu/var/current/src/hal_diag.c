void zpu_if_diag_init(void)
{
}

extern void outbyte(int c);
extern int inbyte();

void 
zpu_if_diag_write_char(char c)
{
	outbyte(c);
}

void 
zpu_if_diag_read_char(char *c)
{
	*c=inbyte();
}

